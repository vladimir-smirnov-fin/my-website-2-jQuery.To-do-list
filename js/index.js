// Ждем, пока весь HTML-документ загрузится
$(document).ready(function () {
  // Создаем пустой массив для хранения задач
  let tasks = [];

  // Обработчик клика по кнопке "Добавить"
  $('#addTask').on('click', function () {
    // Получаем текст из поля ввода (#taskInput) и удаляем лишние пробелы
    const task = $('#taskInput').val().trim();
    // Проверяем, не пустой ли ввод
    if (task) {
      // Добавляем задачу в массив tasks
      tasks.push(task);
      // Очищаем поле ввода
      $('#taskInput').val('');
      // Показываем уведомление (#notification) с плавным появлением
      $('#notification').fadeIn();
      // Через 2 секунды скрываем уведомление с плавным исчезновением
      setTimeout(() => {
        $('#notification').fadeOut();
      }, 2000);
    }
  });

  // Обработчик клика по кнопке "Отобразить"
  $('#showTasks').on('click', function () {
    // Находим список (#taskList) и очищаем его (удаляем все предыдущие элементы)
    const $list = $('#taskList');
    $list.empty();
    // Перебираем массив задач tasks, получая каждую задачу и её индекс
    tasks.forEach((task, index) => {
        // Добавляем в список строку с номером и текстом задачи
        $list.append(`<li>Задание #${index + 1}: ${task}</li>`);
    });
  });
});